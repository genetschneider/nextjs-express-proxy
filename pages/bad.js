import Head from "next/head";
import Link from "next/link";
import { useRouter } from "next/router";
import styles from "../styles/Home.module.css";
import { sleep } from "../utils";

export default function Home({ random, user }) {
  const { locale } = useRouter();

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>
          Welcome to page with bad example of user props!
        </h1>
        <h3 className={styles.title}>SSR: {random}</h3>

		{user ? <p>{user.name}</p> : <></>}

        <Link className={styles.description} locale={locale} href="/">
          Click here to go back!
        </Link>
      </main>
    </div>
  );
}

export const getServerSideProps = async (ctx) => {
  await sleep(250);

  let user = null;

  if (ctx.query.name) {
    user = { name: ctx.query.name };
  }

  return {
    props: {
      random: Date.now(),
      user,
    },
  };
};
